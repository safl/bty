def _quadlet_nbdmux(
    version: str,
    data_dir_abs: Path,
    nbdmux_pw: str = _PLACEHOLDER_PASSWORD,
) -> str:
    """Render the nbdmux Quadlet unit. The compose path wires nbdmux
    to withcache via the compose network (``http://withcache:8081``);
    the Quadlet path has no shared network, so we point at
    ``host.containers.internal:8081`` which Podman resolves to the
    host's default gateway - the withcache Quadlet publishes 8081 on
    the host so this route works without baking in a LAN address."""
    edit_note = (
        "# Edit Environment=NBDMUX_ADMIN_PASSWORD before installing.\n#\n"
        if nbdmux_pw == _PLACEHOLDER_PASSWORD
        else ""
    )
    return f"""\
# Generated by `bty-lab init --systemd` (bty {version}).
# Install: sudo cp this file into /etc/containers/systemd/ then run
#   sudo systemctl daemon-reload && sudo systemctl start nbdmux.service
#
{edit_note}[Unit]
Description=nbdmux NBD-export sidecar (ramboot)
After=network-online.target withcache.service
Wants=network-online.target

[Container]
Image=ghcr.io/safl/nbdmux:latest
AutoUpdate=registry
PublishPort=8082:8082
# nbdmux v0.8.1+ spawns one nbdkit per export starting at 10809 and
# incrementing (10810, 10811, ...) so per-export filter chains can
# diverge (e.g. --filter=partition for partitioned disk images).
# Publish a range so all per-export nbdkit instances are reachable
# from the host. Small lab bound to <10 exports at a time.
PublishPort=10809-10819:10809-10819
Volume={data_dir_abs}/nbdmux:/data:Z
Volume={data_dir_abs}/nbdmux/images:/images:Z
# See note on bty-web's DNS= -- same reason.
DNS=1.1.1.1
Environment=NBDMUX_ADMIN_PASSWORD={nbdmux_pw}
# Route nbdmux -> withcache via the host's default gateway. In the
# compose path this is ``http://withcache:8081`` (compose network
# DNS); Quadlet units have no shared network, so we use Podman's
# host-alias which resolves to the host's default gateway and
# reaches the withcache Quadlet's PublishPort=8081.
Environment=NBDMUX_WITHCACHE_URL=http://host.containers.internal:8081
# Browser-reachable URL for withcache. Nbdmux embeds this in
# operator-facing cross-links only; leave as-is on single-host
# deploys where withcache's 8081 is exposed on localhost.
Environment=NBDMUX_WITHCACHE_BROWSER_URL=http://localhost:8081

[Service]
Restart=always

[Install]
WantedBy=multi-user.target default.target
"""


